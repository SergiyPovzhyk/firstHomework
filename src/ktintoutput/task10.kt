package ktintoutput

fun main(){
    val a:Double = 2.0
    val b :Double = 5.0
    val apow = Math.pow(a,2.0)
    val bpow = Math.pow(b, 2.0)
    val sum = apow + bpow
    val diff = apow - bpow
    val dob = apow * bpow
    val frac = apow / bpow
    println(sum)
    println(diff)
    println(dob)
    println(frac)

}